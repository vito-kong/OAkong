package com.dynastech.cdmetro.beans;

public class AttenceBean {

	private String LocationRepeatInterval;
	private int LocationRepeatCount;
	private int LocationRepeatStart;
	private int LocationRepeatEnd;

	public String getLocationRepeatInterval() {
		return LocationRepeatInterval;
	}

	public void setLocationRepeatInterval(String locationRepeatInterval) {
		LocationRepeatInterval = locationRepeatInterval;
	}

	public int getLocationRepeatCount() {
		return LocationRepeatCount;
	}

	public void setLocationRepeatCount(int locationRepeatCount) {
		LocationRepeatCount = locationRepeatCount;
	}

	public int getLocationRepeatStart() {
		return LocationRepeatStart;
	}

	public void setLocationRepeatStart(int locationRepeatStart) {
		LocationRepeatStart = locationRepeatStart;
	}

	public int getLocationRepeatEnd() {
		return LocationRepeatEnd;
	}

	public void setLocationRepeatEnd(int locationRepeatEnd) {
		LocationRepeatEnd = locationRepeatEnd;
	}

}
